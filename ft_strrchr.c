#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*s1;

	s1 = 0;
	while (*s)
	{
		if (*s == c)
			s1 = (char *)s;
		s++;
	}
	if (c == '\0')
		s1 = (char *)s;
	return (s1);
}
